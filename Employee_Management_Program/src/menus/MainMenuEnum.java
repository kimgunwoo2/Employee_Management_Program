package menus;

public enum MainMenuEnum {
	REGISTER(1, "직원 등록"),
	SEARCH(2, "직원 검색"),
	UPDATE(3,"직원 갱신"),
	EXIT(9, "종료");
	
	private int code;
	private String message;
	
	MainMenuEnum(int code, String message) {
		this.code = code;
		this.message = message;
		//System.out.println("생성자 in MainMenuEnum: " + code);
	}
	
	public int getCode() {
		return code;
	}
	
	public String getMessage() {
		return message;
	}
}
